// ----------------------------------------------------------------------------
// Frontmatter + YAML-subset parsing (replaces PyYAML)
// ----------------------------------------------------------------------------

// Match the opening/closing frontmatter delimiters. Mirrors the Python
// load_markdown split: first '\n---\n' after the opening closes the block.
const FM_SPLIT = /^---[ \t]*\n([\s\S]*?)\n---[ \t]*\n?([\s\S]*)$/;

function stripQuotes(raw) {
  const s = raw.trim();
  if (s.length >= 2 && s[0] === '"' && s[s.length - 1] === '"') {
    return s.slice(1, -1);
  }
  if (s.length >= 2 && s[0] === "'" && s[s.length - 1] === "'") {
    return s.slice(1, -1).replace(/''/g, "'");
  }
  return s;
}

// Parse a frontmatter block into an object. Handles the YAML subset used by the
// catalog: inline scalars (optionally quoted), literal block scalars (key: |),
// block lists (key: then '- item' lines), and nested maps (only so the parser
// does not mis-read a following top-level key; values are not consumed here).
function parseFrontmatter(fmText) {
  const lines = fmText.split('\n');
  const obj = {};
  const TOP = /^([A-Za-z0-9_][A-Za-z0-9_-]*):(.*)$/;
  let i = 0;
  while (i < lines.length) {
    const line = lines[i];
    if (line.trim() === '' || /^\s*#/.test(line)) { i++; continue; }
    const m = line.match(TOP);
    if (!m) { i++; continue; } // indented or non key:value stray line; skip
    const key = m[1];
    const restTrim = m[2].trim();

    if (['|', '|-', '|+', '>', '>-', '>+'].includes(restTrim)) {
      // Literal/folded block scalar: collect more-indented (or blank) lines.
      const block = [];
      i++;
      while (i < lines.length) {
        const l = lines[i];
        if (l.trim() === '') { block.push(''); i++; continue; }
        const indent = l.length - l.trimStart().length;
        if (indent === 0) break;
        block.push(l);
        i++;
      }
      while (block.length && block[block.length - 1] === '') block.pop();
      const indents = block.filter((l) => l.trim() !== '').map((l) => l.length - l.trimStart().length);
      const minIndent = indents.length ? Math.min(...indents) : 0;
      const dedented = block.map((l) => (l === '' ? '' : l.slice(minIndent)));
      if (restTrim.startsWith('>')) {
        // Folded scalar: join consecutive non-blank lines with a space; blank
        // lines become paragraph breaks. Matches PyYAML for prose.
        const parts = [];
        let para = [];
        for (const l of dedented) {
          if (l === '') {
            if (para.length) { parts.push(para.join(' ')); para = []; }
            parts.push('');
          } else {
            para.push(l);
          }
        }
        if (para.length) parts.push(para.join(' '));
        obj[key] = parts.join('\n');
      } else {
        obj[key] = dedented.join('\n');
      }
      continue;
    }

    if (restTrim === '') {
      // Block list or nested map: collect the indented region.
      const block = [];
      i++;
      while (i < lines.length) {
        const l = lines[i];
        if (l.trim() === '') { i++; continue; }
        const indent = l.length - l.trimStart().length;
        if (indent === 0) break;
        block.push(l);
        i++;
      }
      if (block.length && /^-\s+/.test(block[0].trimStart())) {
        // Block sequence. Group lines into items at each '- ' line, then decide
        // scalar list vs list of maps. An item is a map when its content after
        // the dash is a bare 'key: value' pair; quoted scalars never match, so
        // string lists (including quoted strings that contain colons) stay
        // scalar, matching the prior behavior.
        const KV = /^([A-Za-z0-9_][A-Za-z0-9_-]*):\s*(.*)$/;
        const items = [];
        let cur = null;
        for (const l of block) {
          const t = l.trimStart();
          if (/^-\s+/.test(t)) {
            if (cur) items.push(cur);
            cur = { head: t.replace(/^-\s+/, ''), cont: [] };
          } else if (cur && t !== '') {
            cur.cont.push(t);
          }
        }
        if (cur) items.push(cur);

        if (items.length && items.every((it) => KV.test(it.head))) {
          obj[key] = items.map((it) => {
            const map = {};
            for (const line of [it.head, ...it.cont]) {
              const km = line.match(KV);
              if (km) map[km[1]] = stripQuotes(km[2]);
            }
            return map;
          });
        } else {
          obj[key] = items.map((it) => stripQuotes(it.head));
        }
      } else {
        const sub = {};
        for (const l of block) {
          const sm = l.trim().match(TOP);
          if (sm) sub[sm[1]] = stripQuotes(sm[2]);
        }
        obj[key] = sub;
      }
      continue;
    }

    if (restTrim.startsWith('[') && restTrim.endsWith(']')) {
      // Inline (flow) list, e.g. pairs_well_with: [warm, candid].
      const inner = restTrim.slice(1, -1).trim();
      obj[key] = inner === '' ? [] : inner.split(',').map((s) => stripQuotes(s.trim()));
    } else {
      obj[key] = stripQuotes(restTrim);
    }
    i++;
  }
  return obj;
}

// Split a frontmatter markdown string into [frontmatter object, body].
// Normalizes CRLF/CR to LF first (the repo ships CRLF files), matching the
// Python generator so generated output does not depend on line endings.
function loadMarkdown(raw) {
  const text = raw.replace(/\r\n/g, '\n').replace(/\r/g, '\n');
  const m = text.match(FM_SPLIT);
  if (!m) throw new Error('no frontmatter block found');
  return [parseFrontmatter(m[1]), m[2]];
}

function readText(p) {
  return fs.readFileSync(p, 'utf8');
}

// ----------------------------------------------------------------------------
// Catalog model
// ----------------------------------------------------------------------------

function readEntry(axis, entryDir) {
  const [fm, body] = loadMarkdown(readText(path.join(entryDir, 'ENTRY.md')));
  fm._axis = axis;
  fm._body = body;
  return fm;
}

function loadCatalog() {
  const byId = {};
  const byAxis = {};
  for (const axis of AXES) {
    byAxis[axis] = [];
    const axisPath = path.join(TAXONOMY, AXIS_DIR[axis]);
    const names = fs.readdirSync(axisPath).sort();
    for (const name of names) {
      const entryDir = path.join(axisPath, name);
      if (!fs.statSync(entryDir).isDirectory()) continue;
      const entry = readEntry(axis, entryDir);
      byId[entry.id] = entry;
      byAxis[axis].push(entry);
    }
  }
  return { byId, byAxis };
}

