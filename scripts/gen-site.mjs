#!/usr/bin/env node
// Generate Starlight docs pages from the writing-style-catalog catalog.
//
// Zero-dependency Node ESM port of the former scripts/generate_site_pages.py.
// Reads taxonomy/ and examples/ at the repo root and writes Markdown/MDX into
// the Starlight content tree (default site/src/content/docs/), where Starlight's
// autogenerate sidebar renders it.
//
// Two deliberate differences from the Python generator it replaces:
//   1. Inter-page links are RELATIVE (computed from each page's own slug), so the
//      Pages base segment lives ONLY in site/astro.config.mjs (standard clause
//      14.7). There is no base literal in this file.
//   2. The DiffPair import resolves to site/src/components/DiffPair.astro from the
//      content location site/src/content/docs/examples/diff-pairs/<id>.mdx.
//
// Usage:
//   node scripts/gen-site.mjs                 # write into site/src/content/docs
//   node scripts/gen-site.mjs --out DIR       # write into DIR (must be the site
//                                             # content root or a throwaway dir;
//                                             # source dirs are refused)
//   node scripts/gen-site.mjs --lint          # report source-prose fidelity warnings and exit
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

