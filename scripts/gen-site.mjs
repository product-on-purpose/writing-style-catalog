const REPO_ROOT = path.resolve(SCRIPT_DIR, '..');
const TAXONOMY = path.join(REPO_ROOT, 'taxonomy');
const EXAMPLES = path.join(REPO_ROOT, 'examples');
const DEFAULT_OUT = path.join(REPO_ROOT, 'site', 'src', 'content', 'docs');
const RECIPES_DIR = path.join(EXAMPLES, 'horizontal-slices');
const GENERATED_SUBDIRS = ['reference', 'examples', 'recipes', 'templates'];

const AXES = ['voice', 'tone', 'style', 'format'];
const AXIS_DIR = { voice: 'voices', tone: 'tones', style: 'styles', format: 'formats' };
const AXIS_LABEL = { voice: 'Voices', tone: 'Tones', style: 'Styles', format: 'Formats' };
const TOPICS = ['async-standups', 'morning-routine', 'service-database-choice', 'roadmap-deprioritization', 'onboarding-a-new-hire', 'thanking-a-mentor', 'daily-rest-practice', 'retirement-send-off', 'team-milestone-celebration'];

const GENERATED_BANNER =
  '<!-- GENERATED by scripts/gen-site.mjs - do not edit by hand. ' +
  'Edit the source under taxonomy/ or examples/ and regenerate. -->';
const GENERATED_BANNER_MDX =
  '{/* GENERATED by scripts/gen-site.mjs - do not edit by hand. ' +
  'Edit the source under taxonomy/ or examples/ and regenerate. */}';

