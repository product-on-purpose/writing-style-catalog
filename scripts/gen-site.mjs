// Route slug (no leading/trailing slash) for an entry's reference page.
function refSlug(entry) {
  return `reference/${AXIS_DIR[entry._axis]}/${entry.id}`;
}

// Relative URL (with trailing slash) from one page slug to another. Each page
// renders to a directory (<slug>/index.html), so the link resolves against the
// current page's directory URL, which already includes the configured base.
function relUrl(fromSlug, toSlug) {
  const from = fromSlug ? fromSlug.split('/') : [];
  const to = toSlug ? toSlug.split('/') : [];
  let common = 0;
  while (common < from.length && common < to.length && from[common] === to[common]) common++;
  const ups = from.length - common;
  const downs = to.slice(common);
  const rel = '../'.repeat(ups) + downs.map((s) => `${s}/`).join('');
  return rel === '' ? './' : rel;
}

// Markdown link to an entry by id from a given page, using its display name.
// Falls back to a code span if the id is unknown.
function entryLink(catalog, fromSlug, entryId) {
  const entry = catalog.byId[entryId];
  if (!entry) return `\`${entryId}\``;
  return `[${entry.name}](${relUrl(fromSlug, refSlug(entry))})`;
}

