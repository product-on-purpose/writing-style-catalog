// Map topic-slug -> { body, topicLabel } for an entry's vertical slices.
function examplesForEntry(entryId) {
  const out = {};
  for (const topic of TOPICS) {
    const topicDir = path.join(EXAMPLES, 'vertical-slices', topic);
    if (!fs.existsSync(topicDir)) continue;
    const mds = fs.readdirSync(topicDir).filter((f) => f.endsWith('.md')).sort();
    for (const md of mds) {
      const [fm, body] = loadMarkdown(readText(path.join(topicDir, md)));
      if (fm.entry_id === entryId) {
        out[topic] = { body, topicLabel: fm.topic_label || topic };
        break;
      }
    }
  }
  return out;
}

// Diff-pair bodies have three top-level boundary headers, in order:
// "## What to notice", "## A: `id`", "## B: `id`". Passages may contain their
// own nested ## headers, so split ONLY on the boundary headers (matched by text).
const H2 = /^##\s+(.*?)\s*$/gm;

function sectionKey(header) {
  const h = header.toLowerCase();
  if (h.startsWith('what to notice')) return 'what_to_notice';
  if (h.startsWith('a:')) return 'passage_a';
  if (h.startsWith('b:')) return 'passage_b';
  return null;
}

function parseDiffPair(raw) {
  const [fm, body] = loadMarkdown(raw);
  const boundaries = [];
  let mt;
  H2.lastIndex = 0;
  while ((mt = H2.exec(body)) !== null) {
    if (sectionKey(mt[1]) !== null) {
      boundaries.push({ key: sectionKey(mt[1]), start: mt.index, end: mt.index + mt[0].length });
    }
  }
  const sections = {};
  for (let i = 0; i < boundaries.length; i++) {
    const b = boundaries[i];
    const start = b.end;
    const end = i + 1 < boundaries.length ? boundaries[i + 1].start : body.length;
    let text = body.slice(start, end).trim();
    text = text.replace(/\n+---\s*$/, '').trim();
    sections[b.key] = text;
  }
  return {
    entry_a: fm.entry_a,
    entry_b: fm.entry_b,
    axis_varied: fm.axis_varied,
    topic_label: fm.topic_label || '',
    diff_pair_id: fm.diff_pair_id || '',
    what_to_notice: sections.what_to_notice || '',
    passage_a: sections.passage_a || '',
    passage_b: sections.passage_b || '',
  };
}

function walkMarkdown(dir) {
  const out = [];
  if (!fs.existsSync(dir)) return out;
  for (const name of fs.readdirSync(dir)) {
    const full = path.join(dir, name);
    const st = fs.statSync(full);
    if (st.isDirectory()) out.push(...walkMarkdown(full));
    else if (name.endsWith('.md')) out.push(full);
  }
  return out;
}

function loadDiffPairs() {
  const base = path.join(EXAMPLES, 'diff-pairs');
  // Sort by repo-relative POSIX path to match the Python rglob + sorted order.
  const files = walkMarkdown(base)
    .filter((f) => path.basename(f) !== 'README.md')
    .sort((a, b) => {
      const ra = path.relative(base, a).split(path.sep).join('/');
      const rb = path.relative(base, b).split(path.sep).join('/');
      return ra < rb ? -1 : ra > rb ? 1 : 0;
    });
  return files.map((f) => parseDiffPair(readText(f)));
}

function diffPairsForEntry(pairs, entryId) {
  return pairs.filter((p) => p.entry_a === entryId || p.entry_b === entryId);
}

