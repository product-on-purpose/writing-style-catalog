// Rendering helpers
// ----------------------------------------------------------------------------

// Safe single-line YAML scalar for a frontmatter title/description.
function yamlTitle(text) {
  return `"${String(text == null ? '' : text).replace(/"/g, "'")}"`;
}

// Source-prose defect patterns that would render verbatim on the live site.
const FIDELITY_PATTERNS = [
  [/\s\./, 'space before period (orphan period, e.g. stray em-dash sweep)'],
  [/(?<!\.)\.\.(?!\.)/, 'doubled period'],
  [/,,/, 'doubled comma'],
  [/\bTODO\b|\bTKTK\b/, 'unfinished marker'],
];

function fidelityWarnings(text) {
  const hits = [];
  for (const [rx, msg] of FIDELITY_PATTERNS) {
    if (rx.test(text)) hits.push(msg);
  }
  return hits;
}

// Escape `<` and `{` outside code fences/spans so prose embeds safely in MDX.
const FENCE_OR_SPAN = /(^```[\s\S]*?^```|`[^`\n]*`)/gm;

function escapeChunk(s) {
  return s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/\{/g, '&lbrace;');
}

function mdxEscapeProse(text) {
  const parts = [];
  let last = 0;
  let m;
  FENCE_OR_SPAN.lastIndex = 0;
  while ((m = FENCE_OR_SPAN.exec(text)) !== null) {
    parts.push(escapeChunk(text.slice(last, m.index)));
    parts.push(m[0]);
    last = m.index + m[0].length;
    if (m[0].length === 0) FENCE_OR_SPAN.lastIndex++; // guard against zero-width
  }
  parts.push(escapeChunk(text.slice(last)));
  return parts.join('');
}

function xrefBlock(catalog, fromSlug, entry, field, heading) {
  const ids = entry[field] || [];
  if (!ids.length) return '';
  const links = ids.map((id) => entryLink(catalog, fromSlug, id)).join(', ');
  return `### ${heading}\n\n${links}\n\n`;
}

function joinPage(lines) {
  return `${lines.join('\n').replace(/\s+$/, '')}\n`;
}

// Render the ADR 0009 pedagogical fields (tells / anti_patterns / failure_modes)
// as page sections. Frontmatter is the single source of truth; entries that have
// not been backfilled yet simply contribute nothing.
function pedagogySections(entry) {
  const out = [];
  const tells = Array.isArray(entry.tells) ? entry.tells : [];
  if (tells.length) {
    out.push('## Tells', '');
    for (const t of tells) out.push(`- ${mdxEscapeProse(String(t))}`);
    out.push('');
  }
  const anti = Array.isArray(entry.anti_patterns) ? entry.anti_patterns : [];
  if (anti.length) {
    out.push('## Anti-patterns', '');
    for (const a of anti) {
      if (a && a.pattern) {
        out.push(`- **${mdxEscapeProse(String(a.pattern))}** - ${mdxEscapeProse(String(a.why || ''))}`);
      }
    }
    out.push('');
  }
  const fm = Array.isArray(entry.failure_modes) ? entry.failure_modes : [];
  if (fm.length) {
    out.push('## Failure modes', '');
    for (const f of fm) {
      if (f && f.mode) {
        out.push(`- **${mdxEscapeProse(String(f.mode))}** - ${mdxEscapeProse(String(f.mitigation || ''))}`);
      }
    }
    out.push('');
  }
  return out;
}

function renderEntryPage(catalog, pairs, entry) {
  const axis = entry._axis;
  const fromSlug = refSlug(entry);
  const out = [];
  out.push('---');
  out.push(`title: ${yamlTitle(entry.name)}`);
  out.push(`description: ${yamlTitle(entry.one_liner || '')}`);
  out.push('editUrl: false');
  out.push('---');
  out.push('');
  out.push(GENERATED_BANNER_MDX);
  out.push('');
  out.push("import { Tabs, TabItem } from '@astrojs/starlight/components';");
  out.push('');
  out.push(`> ${entry.one_liner || ''}`);
  out.push('');
  out.push((entry._body || '').trim());
  out.push('');

  for (const line of pedagogySections(entry)) out.push(line);

  const phrasing = (entry.llm_instruction_phrasing || '').trim();
  if (phrasing) {
    out.push('## Instruction');
    out.push('');
    out.push('```text');
    out.push(phrasing);
    out.push('```');
    out.push('');
  }

  if (axis === 'format' && entry.canonical_template) {
    out.push('## Template');
    out.push('');
    out.push(`See the [${entry.name} template](${relUrl(fromSlug, `templates/${entry.id}`)}).`);
    out.push('');
  }

  let xref = '';
  xref += xrefBlock(catalog, fromSlug, entry, 'pairs_well_with', 'Pairs well with');
  xref += xrefBlock(catalog, fromSlug, entry, 'avoid_with', 'Avoid with');
  xref += xrefBlock(catalog, fromSlug, entry, 'confusable_with', 'Often confused with');
  if (xref) {
    out.push('## Related');
    out.push('');
    out.push(xref.replace(/\s+$/, ''));
    out.push('');
  }

  const examples = examplesForEntry(entry.id);
  if (Object.keys(examples).length) {
    out.push('## Examples');
    out.push('');
    out.push('<Tabs>');
    for (const topic of TOPICS) {
      const rec = examples[topic];
      if (!rec) continue;
      out.push(`<TabItem label="${rec.topicLabel}">`);
      out.push('');
      out.push(mdxEscapeProse(rec.body.trim()));
      out.push('');
      out.push('</TabItem>');
    }
    out.push('</Tabs>');
    out.push('');
  }

  const appears = diffPairsForEntry(pairs, entry.id);
  if (appears.length) {
    out.push('## Appears in diff-pairs');
    out.push('');
    for (const p of appears) {
      const other = p.entry_a === entry.id ? p.entry_b : p.entry_a;
      out.push(
        `- [${entry.id} vs ${other}]` +
          `(${relUrl(fromSlug, `examples/diff-pairs/${p.diff_pair_id}`)}) ` +
          `(varies ${p.axis_varied})`
      );
    }
    out.push('');
  }

  return joinPage(out);
}

function renderDiffPairPage(catalog, dp) {
  const fromSlug = `examples/diff-pairs/${dp.diff_pair_id}`;
  const aLink = entryLink(catalog, fromSlug, dp.entry_a);
  const bLink = entryLink(catalog, fromSlug, dp.entry_b);
  const ea = dp.entry_a;
  const eb = dp.entry_b;
  const out = [
    '---',
    `title: ${yamlTitle(`${ea} vs ${eb}`)}`,
    `description: ${yamlTitle(dp.topic_label)}`,
    'editUrl: false',
    '---',
    '',
    GENERATED_BANNER_MDX,
    '',
    `import DiffPair from '${DIFF_PAIR_IMPORT}';`,
    '',
    `**Topic:** ${dp.topic_label}  `,
    `**Axis varied:** ${dp.axis_varied}  `,
    `**A:** ${aLink}  **B:** ${bLink}`,
    '',
    '## What to notice',
    '',
    mdxEscapeProse(dp.what_to_notice),
    '',
    `<DiffPair labelA="A: ${ea}" labelB="B: ${eb}">`,
    '<div slot="a">',
    '',
    mdxEscapeProse(dp.passage_a),
    '',
    '</div>',
    '<div slot="b">',
    '',
    mdxEscapeProse(dp.passage_b),
    '',
    '</div>',
    '</DiffPair>',
    '',
  ];
  return joinPage(out);
}

function renderTemplatePage(fmt) {
  const fromSlug = `templates/${fmt.id}`;
  const template = (fmt.canonical_template || '').replace(/\s+$/, '');
  const out = [
    '---',
    `title: ${yamlTitle(`${fmt.name} template`)}`,
    `description: ${yamlTitle(`Canonical template for the ${fmt.name} format.`)}`,
    'editUrl: false',
    '---',
    '',
    GENERATED_BANNER,
    '',
    `Canonical template for the [${fmt.name}](${relUrl(fromSlug, refSlug(fmt))}) format.`,
    '',
    '```markdown',
    template,
    '```',
    '',
  ];
  return joinPage(out);
}

// Replace `id` backtick tokens in a recipe README with entry links.
function linkEntryNamesInTable(catalog, fromSlug, text) {
  return text.replace(/`([a-z][a-z0-9-]*)`/g, (full, token) =>
    catalog.byId[token] ? entryLink(catalog, fromSlug, token) : full
  );
}

// Convert relative links to local .md files into plain text (their targets are
// not generated into the site, so the links would 404). Leaves http(s) and
// site-absolute links intact.
function stripLocalMdLinks(text) {
  return text.replace(/\[([^\]]+)\]\(([^)]+)\)/g, (full, label, target) => {
    if (target.startsWith('http') || target.startsWith('/')) return full;
    if (target.split('#')[0].endsWith('.md')) return label;
    return full;
  });
}

function renderRecipePage(catalog, recipeSlug) {
  const readme = readText(path.join(RECIPES_DIR, recipeSlug, 'README.md')).replace(/\r\n/g, '\n').replace(/\r/g, '\n');
  const lines = readme.split('\n');
  const heading = lines.length ? lines[0].replace(/^#\s*/, '').replace(/^#+/, '').trim() : recipeSlug;
  let body = lines.slice(1).join('\n').trim();
  body = stripLocalMdLinks(body);
  body = linkEntryNamesInTable(catalog, `recipes/${recipeSlug}`, body);
  const out = [
    '---',
    `title: ${yamlTitle(heading)}`,
    'editUrl: false',
    '---',
    '',
    GENERATED_BANNER,
    '',
    body,
    '',
  ];
  return joinPage(out);
}

function listRecipes() {
  if (!fs.existsSync(RECIPES_DIR)) return [];
  return fs
    .readdirSync(RECIPES_DIR)
    .filter((d) => {
      const full = path.join(RECIPES_DIR, d);
      return fs.statSync(full).isDirectory() && fs.existsSync(path.join(full, 'README.md'));
    })
    .sort();
}

function page(title, description, body) {
  const out = [
    '---',
    `title: ${yamlTitle(title)}`,
    `description: ${yamlTitle(description)}`,
    'editUrl: false',
    '---',
    '',
    GENERATED_BANNER,
    '',
    body.trim(),
    '',
  ];
  return joinPage(out);
}

function renderReferenceIndex(catalog) {
  const fromSlug = 'reference';
  const parts = [];
  for (const axis of AXES) {
    parts.push(`## ${AXIS_LABEL[axis]}`);
    parts.push('');
    for (const e of catalog.byAxis[axis]) {
      parts.push(`- [${e.name}](${relUrl(fromSlug, refSlug(e))}) - ${e.one_liner || ''}`);
    }
    parts.push('');
  }
  return page('Reference', 'Every entry across the four axes.', parts.join('\n'));
}

function renderDiffPairIndex(pairs) {
  const fromSlug = 'examples/diff-pairs';
  const byAxis = {};
  for (const p of pairs) (byAxis[p.axis_varied] = byAxis[p.axis_varied] || []).push(p);
  const parts = [];
  for (const axis of Object.keys(byAxis).sort()) {
    parts.push(`## Varying ${axis}`);
    parts.push('');
    for (const p of byAxis[axis]) {
      parts.push(
        `- [${p.entry_a} vs ${p.entry_b}]` +
          `(${relUrl(fromSlug, `examples/diff-pairs/${p.diff_pair_id}`)}) - ${p.topic_label}`
      );
    }
    parts.push('');
  }
  return page('Diff-pairs', 'Side-by-side single-axis contrasts.', parts.join('\n'));
}

function renderRecipeIndex() {
  const fromSlug = 'recipes';
  const parts = [];
  for (const slug of listRecipes()) {
    parts.push(`- [${slug}](${relUrl(fromSlug, `recipes/${slug}`)})`);
  }
  return page('Recipes', 'Curated four-axis compositions.', parts.join('\n'));
}

function renderTemplateIndex(catalog) {
  const fromSlug = 'templates';
  const parts = [];
  for (const e of catalog.byAxis.format) {
    if (e.canonical_template) parts.push(`- [${e.name}](${relUrl(fromSlug, `templates/${e.id}`)})`);
  }
  return page('Templates', 'Canonical format templates.', parts.join('\n'));
}

function renderExamplesTopicIndex(topic) {
  const body =
    'Vertical-slice examples for this topic, one per entry. ' +
    "Each is also embedded on its entry's reference page.\n";
  return page(topic, `Examples for the ${topic} anchor topic.`, body);
}

