function write(outRoot, rel, content) {
  const p = path.join(outRoot, rel);
  fs.mkdirSync(path.dirname(p), { recursive: true });
  fs.writeFileSync(p, content, 'utf8');
}

// Guard the destructive cleanup below. The output root must be a throwaway or
// the site content directory, never a repo source tree: without this,
// `gen-site.mjs --out .` from the repo root would clear outRoot/examples, which
// is the real examples/ source tree (data loss). Refuses outRoot when it is, is
// an ancestor of, or sits inside a protected source directory, or when clearing
// any generated subtree would hit one.
function assertSafeOutRoot(outRoot) {
  const abs = path.resolve(outRoot);
  const root = path.resolve(REPO_ROOT);
  const sourceDirs = ['taxonomy', 'examples', 'docs', 'schemas', 'scripts', 'tools', 'tests', 'skills'].map((d) =>
    path.join(root, d)
  );
  const insideOrEqual = (p, r) => p === r || p.startsWith(r + path.sep);
  const cleared = GENERATED_SUBDIRS.map((s) => path.join(abs, s));
  const unsafe =
    abs === root ||
    root.startsWith(abs + path.sep) ||
    sourceDirs.some((r) => insideOrEqual(abs, r)) ||
    cleared.some((c) => sourceDirs.some((r) => insideOrEqual(c, r)));
  if (unsafe) {
    throw new Error(
      `gen-site: refusing to generate into '${abs}'. It is, contains, or sits inside a protected ` +
        'source directory, so clearing the generated subtrees could delete real source. Use the ' +
        'default output (site/src/content/docs) or a throwaway directory.'
    );
  }
}

function generate(outRoot) {
  assertSafeOutRoot(outRoot);
  const catalog = loadCatalog();
  const pairs = loadDiffPairs();
  let n = 0;

  // Clear previously generated subtrees first, so an in-place rebuild (the
  // catalog is gitignored and not drift-guarded) cannot leave a stale page
  // whose source was removed. Only generated dirs are cleared; the hand-authored
  // narrative under the same content root is untouched.
  for (const sub of GENERATED_SUBDIRS) {
    fs.rmSync(path.join(outRoot, sub), { recursive: true, force: true });
  }

  // Entry pages (.mdx because they import components / use <Tabs>).
  for (const axis of AXES) {
    for (const entry of catalog.byAxis[axis]) {
      write(outRoot, `reference/${AXIS_DIR[axis]}/${entry.id}.mdx`, renderEntryPage(catalog, pairs, entry));
      n += 1;
    }
  }
  write(outRoot, 'reference/index.md', renderReferenceIndex(catalog));

  // Diff-pair pages (.mdx, import DiffPair).
  for (const dp of pairs) {
    write(outRoot, `examples/diff-pairs/${dp.diff_pair_id}.mdx`, renderDiffPairPage(catalog, dp));
    n += 1;
  }
  write(outRoot, 'examples/diff-pairs/index.md', renderDiffPairIndex(pairs));

  // Example topic index pages.
  for (const topic of TOPICS) {
    write(outRoot, `examples/${topic}/index.md`, renderExamplesTopicIndex(topic));
  }

  // Recipes.
  for (const slug of listRecipes()) {
    write(outRoot, `recipes/${slug}.md`, renderRecipePage(catalog, slug));
    n += 1;
  }
  write(outRoot, 'recipes/index.md', renderRecipeIndex());

  // Templates.
  for (const e of catalog.byAxis.format) {
    if (e.canonical_template) {
      write(outRoot, `templates/${e.id}.md`, renderTemplatePage(e));
      n += 1;
    }
  }
  write(outRoot, 'templates/index.md', renderTemplateIndex(catalog));

  return n;
}

function runLint() {
  const catalog = loadCatalog();
  let total = 0;
  for (const axis of AXES) {
    for (const e of catalog.byAxis[axis]) {
      for (const w of fidelityWarnings(e._body || '')) {
        console.log(`taxonomy/${AXIS_DIR[axis]}/${e.id}/ENTRY.md: ${w}`);
        total += 1;
      }
    }
  }
  for (const md of walkMarkdown(path.join(EXAMPLES, 'vertical-slices'))) {
    const [, body] = loadMarkdown(readText(md));
    for (const w of fidelityWarnings(body)) {
      console.log(`${path.relative(REPO_ROOT, md).split(path.sep).join('/')}: ${w}`);
      total += 1;
    }
  }
  console.log(`[lint] ${total} fidelity warning(s)`);
}

function main() {
  const args = process.argv.slice(2);
  if (args.includes('--lint')) {
    runLint();
    return;
  }
  let outRoot = DEFAULT_OUT;
  const outIdx = args.indexOf('--out');
  if (outIdx !== -1 && args[outIdx + 1]) outRoot = path.resolve(args[outIdx + 1]);
  const count = generate(outRoot);
  console.log(`[OK] generated ${count} catalog pages into ${outRoot}`);
}

